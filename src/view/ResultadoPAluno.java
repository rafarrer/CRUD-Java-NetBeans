    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
        AlunoDAO dao = new AlunoDAO();
        
        int linha = jTable1.getSelectedRow();
        
        Aluno p = alunosNaTabela.get(linha);
        p.setNome(txtNome.getText());
        p.setRa(Integer.parseInt(txtRa.getText()));
        p.setPrevisaoFormatura(txtPrevisao.getText());
        
        try {
            dao.alterar(p);
            txtNome.setText("");
            txtRa.setText("");
            txtPrevisao.setText("");
            //this.dispose();
            rewriteTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar: " +ex);
        }

    }//GEN-LAST:event_atualizarActionPerformed

    private void deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarActionPerformed
        if(jTable1.getSelectedRow() != -1) {        
            AlunoDAO dao = new AlunoDAO();
            int linha = jTable1.getSelectedRow();
            Aluno p = alunosNaTabela.get(linha);
            try {
                dao.delete(p);
                alunosNaTabela.remove(p);
                rewriteTable();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao alterar: " +ex);
            }   
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela para excluir!");
        }
    }//GEN-LAST:event_deletarActionPerformed

