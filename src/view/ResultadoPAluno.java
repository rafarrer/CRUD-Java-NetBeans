    private void rewriteTable(){
        DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
            tableModel.setNumRows(alunosNaTabela.size());
            int i = 0;
            for (Aluno aluno : alunosNaTabela) {
                tableModel.setValueAt(aluno.getNome(), i, 0);
                tableModel.setValueAt(aluno.getRa(), i, 1);
                tableModel.setValueAt(aluno.getPrevisaoFormatura(), i, 2);
                i++;
            }
    }
