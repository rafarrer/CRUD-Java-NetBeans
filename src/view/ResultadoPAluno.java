    public void efetuaPesquisa(String nome){
        AlunoDAO dao = new AlunoDAO();
        try { 
            if (!nome.isEmpty())
                alunosNaTabela = dao.pesquisar(nome);
            else
                alunosNaTabela = dao.pesquisar();
            rewriteTable();
        } catch (SQLException e) {
        }
    }

